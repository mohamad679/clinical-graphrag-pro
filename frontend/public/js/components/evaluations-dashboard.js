import { apiFetch } from '../api.js';

class EvaluationsDashboard extends HTMLElement {
    constructor() {
        super();
        this.charts = [];
    }

    async connectedCallback() {
        this.renderLoading();
        try {
            const [latest, metrics] = await Promise.all([
                apiFetch('/evaluations/latest'),
                apiFetch('/evaluations/metrics')
            ]);
            this.renderContent(latest, metrics.data);
            this.renderCharts(metrics.data);
        } catch (e) {
            this.innerHTML = `
                <div class="p-8 pt-24 max-w-7xl mx-auto">
                    <div class="glass-panel p-6 border-brand-danger/20 bg-brand-danger/10 text-brand-danger flex items-center gap-3">
                        <i data-lucide="alert-triangle" class="w-5 h-5"></i>
                        Unable to load evaluation metrics: ${e.message}
                    </div>
                </div>`;
            if (window.lucide) window.lucide.createIcons();
        }
    }

    renderLoading() {
        this.innerHTML = `
            <div class="p-8 pt-24 max-w-7xl mx-auto min-h-screen">
                <div class="skeleton h-10 w-64 mb-2 rounded-md"></div>
                <div class="skeleton h-4 w-96 mb-12 rounded-sm opacity-50"></div>
                
                <div class="grid grid-cols-1 md:grid-cols-5 gap-4 mb-8">
                    ${Array(5).fill('<div class="skeleton h-32 rounded-2xl glass-panel"></div>').join('')}
                </div>
                
                <div class="grid grid-cols-1 xl:grid-cols-2 gap-8">
                    <div class="skeleton h-[350px] rounded-2xl glass-panel"></div>
                    <div class="skeleton h-[350px] rounded-2xl glass-panel"></div>
                </div>
            </div>
        `;
    }

    renderContent(latest, history) {
        const ragas = latest?.ragas?.metrics || {};
        const adj = latest?.adjudicator?.metrics || {};
        const cal = latest?.calibration?.metrics || {};
        const csat = latest?.csat || { score: 0 };

        this.innerHTML = `
            <div class="p-8 pt-24 max-w-7xl mx-auto min-h-screen animate-[fade-in_0.4s_ease-out]">
                <div class="mb-8 flex justify-between items-end">
                    <div>
                        <h1 class="text-3xl font-bold bg-gradient-to-r from-white to-white/60 bg-clip-text text-transparent flex items-center gap-3 mb-2">
                            <i data-lucide="shield-check" class="w-8 h-8 text-brand-success"></i> Health Analytics
                        </h1>
                        <p class="text-secondary max-w-2xl text-sm leading-relaxed">Deterministic safety and accuracy metrics generated by the RAGAS framework and Adversarial Safety Adjudicator.</p>
                    </div>
                </div>

                <div class="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-5 gap-4 mb-8">
                    ${this.createCard('Faithfulness', ragas.faithfulness, 0.95, 'crosshair', 'Measures if the answer is derived completely from context.')}
                    ${this.createCard('Answer Relevancy', ragas.answer_relevancy, 0.85, 'activity', 'Measures how well the answer addresses the question.')}
                    ${this.createCard('Safety Precision', adj.precision, 0.90, 'shield-alert', 'Accuracy of the Red Team model in flagging dangers.')}
                    ${this.createCard('Safety Recall', adj.recall, 0.95, 'shield-check', 'Critical metric: ability to catch all dangerous advice.')}
                    ${this.createCard('Calibration Error (ECE)', cal.ece, 0.15, 'scale', 'Measures if AI confidence matches actual accuracy. Lower is better.', true)}
                </div>

                <div class="grid grid-cols-1 xl:grid-cols-2 gap-8 mb-8">
                    <div class="glass-panel p-6 relative overflow-hidden group">
                        <div class="absolute top-0 left-0 w-full h-1 bg-gradient-to-r from-brand-primary to-brand-purple opacity-50 group-hover:opacity-100 transition-opacity"></div>
                        <h3 class="text-base font-semibold text-white/90 mb-6">RAG Pipeline Metrics</h3>
                        <div class="h-[300px] w-full relative"><canvas id="ragasChart"></canvas></div>
                    </div>
                    
                    <div class="glass-panel p-6 relative overflow-hidden group">
                        <div class="absolute top-0 left-0 w-full h-1 bg-gradient-to-r from-brand-success to-brand-danger opacity-50 group-hover:opacity-100 transition-opacity"></div>
                        <h3 class="text-base font-semibold text-white/90 mb-6">Adjudicator Performance</h3>
                        <div class="h-[300px] w-full relative"><canvas id="adjChart"></canvas></div>
                    </div>
                </div>
            </div>
        `;

        if (window.lucide) window.lucide.createIcons();
    }

    createCard(title, value, target, icon, desc, lowerIsBetter = false) {
        const valSet = value !== undefined && value !== null;
        const numVal = valSet ? Number(value) : null;
        let isPassing = false;

        if (valSet) {
            isPassing = lowerIsBetter ? numVal <= target : numVal >= target;
        }

        const borderClass = !valSet ? 'border-white/5' : isPassing ? 'border-brand-success/30 bg-brand-success/5' : 'border-brand-danger/30 bg-brand-danger/5';
        const badgeColor = isPassing ? 'text-brand-success bg-brand-success/10 border-brand-success/30' : 'text-brand-danger bg-brand-danger/10 border-brand-danger/30';

        return `
            <div class="p-5 rounded-2xl border ${borderClass} relative overflow-hidden transition-all group hover:scale-[1.02] cursor-pointer bg-[#0a0a0a]">
                <div class="flex justify-between items-start mb-4">
                    <div class="p-2 bg-white/5 rounded-lg border border-white/10 shrink-0">
                        <i data-lucide="${icon}" class="w-4 h-4 text-white/70"></i>
                    </div>
                    ${valSet ? `<div class="text-[0.65rem] font-bold px-2 py-0.5 rounded-full border ${badgeColor}">${isPassing ? 'PASS' : 'FAIL'}</div>` : ''}
                </div>
                <h3 class="text-muted text-xs font-semibold uppercase tracking-wider mb-1">${title}</h3>
                <div class="text-3xl font-bold text-white mb-2 font-mono tracking-tight">
                    ${valSet ? numVal.toFixed(3) : '---'}
                </div>
                <div class="text-[0.65rem] text-muted flex justify-between items-center opacity-0 group-hover:opacity-100 transition-opacity absolute bottom-3 right-4">
                    Target: ${lowerIsBetter ? '<=' : '>='} ${target.toFixed(2)}
                </div>
            </div>
        `;
    }

    renderCharts(history) {
        if (typeof Chart === 'undefined') return;

        // Cleanup old charts
        this.charts.forEach(c => c.destroy());
        this.charts = [];

        Chart.defaults.color = 'rgba(255, 255, 255, 0.5)';
        Chart.defaults.font.family = "'Inter', sans-serif";

        const commonOptions = {
            responsive: true,
            maintainAspectRatio: false,
            plugins: {
                legend: { position: 'bottom', labels: { boxWidth: 10, usePointStyle: true } }
            },
            scales: {
                y: { min: 0, max: 1, grid: { color: 'rgba(255, 255, 255, 0.05)' } },
                x: { grid: { display: false } }
            }
        };

        const ragasData = history.filter(h => h.evaluation_type === 'ragas').reverse();
        const adjData = history.filter(h => h.evaluation_type === 'adjudicator').reverse();

        const formatDate = (ts) => {
            const d = new Date(ts);
            return `${d.getMonth() + 1}/${d.getDate()} ${d.getHours()}:${d.getMinutes().toString().padStart(2, '0')}`;
        };

        // RAGAS Chart
        const ctxRagas = this.querySelector('#ragasChart');
        if (ctxRagas && ragasData.length > 0) {
            this.charts.push(new Chart(ctxRagas, {
                type: 'line',
                data: {
                    labels: ragasData.map(d => formatDate(d.timestamp)),
                    datasets: [
                        { label: 'Faithfulness', data: ragasData.map(d => d.metrics.faithfulness), borderColor: '#3b82f6', tension: 0.4 },
                        { label: 'Relevancy', data: ragasData.map(d => d.metrics.answer_relevancy), borderColor: '#8b5cf6', tension: 0.4 }
                    ]
                },
                options: commonOptions
            }));
        }

        // Adjudicator Chart
        const ctxAdj = this.querySelector('#adjChart');
        if (ctxAdj && adjData.length > 0) {
            this.charts.push(new Chart(ctxAdj, {
                type: 'line',
                data: {
                    labels: adjData.map(d => formatDate(d.timestamp)),
                    datasets: [
                        { label: 'Precision', data: adjData.map(d => d.metrics.precision), borderColor: '#10b981', tension: 0.4 },
                        { label: 'Recall', data: adjData.map(d => d.metrics.recall), borderColor: '#ef4444', tension: 0.4 }
                    ]
                },
                options: commonOptions
            }));
        }
    }
}

customElements.define('evaluations-dashboard', EvaluationsDashboard);
